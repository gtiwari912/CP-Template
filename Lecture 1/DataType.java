public class DataType {
    public static void main(String[] args) {
        // Numeric data type.
        byte verySmallNumber = 7;
        short smallNumber = 30000;
        int number = 1000002344;
        long largeNumber = 10000000000000L; //make a note that while assigning long we write 'L' at last, to signify that the number is long.
        float goldWeight = 10.03342f;    //make a note that while assigning long we write 'F' at last, to signify that the number is float.
        double pi = 3.141592653589793238;

        //boolean data
        boolean isAbove18 = false;
        boolean isMale = true;

        //character data;
        char firstLetter = 'a';    // note characters are always written in single quotes;
        char firstCapitalLetter = 'A';
        char space = ' ';
        char dollarSymbol = '$';

        //Textual Data - Called as "String"
        String myName = "Gaurav";  // String data is just the group of characters.
    }
}
